#Lesson 02 - Data types

#String
print("Hello World")
print('Hello World')
print('''Hello World''')
print("""Hello World""")
print(type("Hello World"))

#Integer
print(123456)
print(type(123456))

#Float  - Decimal
print(123.456)
print(type(123.456))

#Boolean
print(True)
print(False)
print(type(True))

#Complex
print(complex(5, 10))
print(type(complex(5, 10)))

#list
print([10, 20, 30, 40, 50])
print(type([10, 20, 30, 40, 50]))

#tuple
print((10, 20, 30, 40, 50))
print(type((10, 20, 30, 40, 50)))

#range
print(range(0, 10))
print(type(range(0, 10)))

#dict
print({"nome": "João", "idade": 25})
print(type({"nome": "João", "idade": 25}))

#set
print({10, 20, 30, 40, 50})
print(type({10, 20, 30, 40, 50}))

#frozenset
print(frozenset({10, 20, 30, 40, 50}))
